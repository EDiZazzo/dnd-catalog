  static String translate(String text, String lang) {
    if (lang == 'en') return text;
    
    final cleanText = text.trim();
    if (lang == 'it') {
      if (_it.containsKey(cleanText)) {
        return _it[cleanText]!;
      }
      
      // Check if it matches "Level X"
      if (cleanText.startsWith('Level ')) {
        final lvl = cleanText.substring(6);
        return 'Livello $lvl';
      }
      
      // Check if it matches "Subclass of X"
      if (cleanText.startsWith('Subclass of ')) {
        final className = cleanText.substring(12);
        final translatedClass = translate(className, lang);
        return 'Sottoclasse di $translatedClass';
      }
    } else if (lang == 'da') {
      if (_da.containsKey(cleanText)) {
        return _da[cleanText]!;
      }
      
      // Check if it matches "Level X"
      if (cleanText.startsWith('Level ')) {
        final lvl = cleanText.substring(6);
        return 'Niveau $lvl';
      }
      
      // Check if it matches "Subclass of X"
      if (cleanText.startsWith('Subclass of ')) {
        final className = cleanText.substring(12);
        final translatedClass = translate(className, lang);
        return 'Underklasse til $translatedClass';
      }
    }
    
    // If it contains a comma-separated list, translate parts
    if (cleanText.contains(',')) {
      return cleanText.split(',').map((s) => translate(s, lang)).join(', ');
    }
    
    return text;
  }
